package Ct.ArrayList응용예제;

import java.math.BigDecimal;

public class ProductMain {
        String name;
        BigDecimal price;

        public ProductMain(String name, BigDecimal price) {
            this.name = name;
            this.price = price;
        }

        public String getName(String name) {
            return name;
        }

        public BigDecimal getPrice(BigDecimal price) {
            return price;
        }
}
